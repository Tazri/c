#include <stdio.h>

int main(void){

    // relational operator on c 
    // relational operator return 0 or 1;
    printf("3 == 4 = %d\n",3==4); // 0 is equal
    printf("3 != 4 = %d\n",3!=4); // 1 is not equal
    printf("3 > 4 = %d\n",3>4); // 0 is greaterthen
    printf("3 < 4 = %d\n",3<4); // 1 is lessthen
    printf("3 >= 4 = %d\n",3>=4); // 0 is greaterthen or equal
    printf("3 <= 4 = %d\n",3<=4); // 1 is lessthen or equal


    return 0;
}